	public static void update() {
		switch(state) {
		case MENU: 
			if(menu == null) 
				menu = new Menu();
				menu.update();
			break;
		case SIM: 
			if(sim == null)
				sim = new SimManager(map);
				sim.update();		
		break;
		
		case MAPEDIT:
			if(mapEdit == null) 
				mapEdit = new MapEdit();
				mapEdit.update();
			break;
		
		}
	}
	
